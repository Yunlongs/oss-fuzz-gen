def normalize_name(name: str) -> str:
    normalized = re.sub(r"[^a-zA-Z0-9]+", "_", name.strip()).strip("_").lower()
    return re.sub(r"_+", "_", normalized)


def list_project_entries(project: str) -> list[tuple[str, Path]]:
    out_base = BUILD_BASE / "out"
    if not out_base.is_dir():
        return []

    prefix = project + "-"
    entries = sorted(
        entry.name
        for entry in out_base.iterdir()
        if entry.is_dir() and entry.name.startswith(prefix)
    )
    return [(entry, out_base / entry) for entry in entries]


def entry_api_name(entry: str, project: str) -> str:
    prefix = project + "-"
    body = entry[len(prefix):] if entry.startswith(prefix) else entry
    match = re.match(r"^(.*)-(\d+)$", body)
    if match:
        body = match.group(1)
    return body


def get_project_target_name(project: str) -> str | None:
    """Load target_name directly from project's YAML file."""
    yaml_path = BENCHMARK_ALL_API_DIR / project / f"{project}.yaml"
    if not yaml_path.is_file():
        return None

    with yaml_path.open("r", encoding="utf-8") as f:
        data = yaml.safe_load(f) or {}

    target_name = data.get("target_name")
    return target_name if isinstance(target_name, str) and target_name.strip() else None


def load_project_api_names(project: str) -> set[str]:
    yaml_path = BENCHMARK_ALL_API_DIR / project / f"{project}.yaml"
    if not yaml_path.is_file():
        return set()

    with yaml_path.open("r", encoding="utf-8") as f:
        data = yaml.safe_load(f) or {}

    functions = data.get("functions", [])
    names = set()
    for item in functions:
        name = item.get("name") if isinstance(item, dict) else None
        if isinstance(name, str) and name.strip():
            names.add(normalize_name(name))
    return names


def _discover_fuzzer_candidates(out_dir: Path) -> set[str]:
    candidates: set[str] = set()

    # Most reliable signal: fuzzer_stats/<fuzzer>.json generated by AFL runs.
    stats_dir = out_dir / "fuzzer_stats"
    if stats_dir.is_dir():
        for stat_file in stats_dir.glob("*.json"):
            if stat_file.name == "coverage_targets.json":
                continue
            candidates.add(stat_file.stem)
    if candidates:
        return candidates

    for child in out_dir.iterdir() if out_dir.is_dir() else []:
        name = child.name

        if child.is_dir() and name.endswith("_afl_address_out"):
            candidates.add(name[: -len("_afl_address_out")])
            continue

        if child.is_file() and name.endswith("_seed_corpus.zip"):
            candidates.add(name[: -len("_seed_corpus.zip")])
            continue

        if not child.is_file():
            continue

        if name.startswith("afl-"):
            continue
        if name.endswith((".dict", ".options", ".zip", ".a", ".o", ".so", ".txt", ".log")):
            continue
        if not child.stat().st_mode & 0o111:
            continue

        # Keep only likely fuzzer binaries.
        low = name.lower()
        if "fuzz" in low or low.endswith("_test"):
            candidates.add(name)

    return candidates

